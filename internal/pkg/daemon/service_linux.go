//go:build linux || android

package daemon

import (
	"fmt"
	"os"
	"os/exec"
	"path/filepath"
	"strings"
)

// service_linux.go provides the *headless* backends — only used when
// env.HasGUI returns false (Termux on Android, Linux server / RPi
// without DISPLAY). The GUI path is handled in service.go via
// internal/autostart for desktop Linux with X / Wayland.
//
// Two backends dispatched at runtime:
//
//	Termux on Android → ~/.termux/boot/<appname>-start (shell script)
//	Headless Linux    → ~/.config/systemd/user/<appname>.service
//
// User-scope only to avoid sudo. The trade-off for systemd-user is
// that the daemon stops at logout unless `loginctl enable-linger` is
// run — documented in the status output.

func isTermux() bool {
	if os.Getenv("TERMUX_VERSION") != "" {
		return true
	}
	return pathExists("/data/data/com.termux/files/usr")
}

func installHeadless(p Paths, appName string) error {
	if isTermux() {
		return installTermux(p, appName)
	}
	return installSystemd(p, appName)
}

func uninstallHeadless(appName string) error {
	if isTermux() {
		return uninstallTermux(appName)
	}
	return uninstallSystemd(appName)
}

func statusHeadless(appName string) (ServiceState, error) {
	if isTermux() {
		return termuxStatus(appName)
	}
	return systemdStatus(appName)
}

// ── Termux backend ─────────────────────────────────────────────

func termuxBootPath(appName string) (string, error) {
	home, err := os.UserHomeDir()
	if err != nil {
		return "", err
	}
	return filepath.Join(home, ".termux", "boot", appName+"-start"), nil
}

func installTermux(p Paths, appName string) error {
	target, err := termuxBootPath(appName)
	if err != nil {
		return err
	}
	if err := os.MkdirAll(filepath.Dir(target), 0o755); err != nil {
		return err
	}
	script := fmt.Sprintf("#!/data/data/com.termux/files/usr/bin/sh\n"+
		"# Auto-generated by %s — runs at Termux boot via Termux:Boot addon.\n"+
		"exec %q start\n", appName, p.ExePath)
	return os.WriteFile(target, []byte(script), 0o755)
}

func uninstallTermux(appName string) error {
	target, err := termuxBootPath(appName)
	if err != nil {
		return err
	}
	if !pathExists(target) {
		return ErrNotInstalled
	}
	return os.Remove(target)
}

func termuxStatus(appName string) (ServiceState, error) {
	target, err := termuxBootPath(appName)
	if err != nil {
		return ServiceState{}, err
	}
	st := ServiceState{
		Path:    target,
		Backend: "termux-boot",
		Note:    "requires the Termux:Boot addon installed and granted boot permission",
	}
	st.Installed = pathExists(target)
	return st, nil
}

// ── systemd user backend ───────────────────────────────────────

func systemdUnitPath(appName string) (string, error) {
	home, err := os.UserHomeDir()
	if err != nil {
		return "", err
	}
	return filepath.Join(home, ".config", "systemd", "user", appName+".service"), nil
}

func installSystemd(p Paths, appName string) error {
	target, err := systemdUnitPath(appName)
	if err != nil {
		return err
	}
	if err := os.MkdirAll(filepath.Dir(target), 0o755); err != nil {
		return err
	}
	unit := fmt.Sprintf(`[Unit]
Description=%s daemon
After=network-online.target
Wants=network-online.target

[Service]
Type=simple
ExecStart=%s all
Restart=on-failure
RestartSec=5
StandardOutput=append:%s
StandardError=append:%s

[Install]
WantedBy=default.target
`, appName, p.ExePath, p.LogFile, p.LogFile)
	if err := os.WriteFile(target, []byte(unit), 0o644); err != nil {
		return err
	}
	// daemon-reload picks up the new unit. enable wires it into boot.
	// Failures here are non-fatal — user can run the commands manually
	// if the host's systemctl is unavailable.
	_ = exec.Command("systemctl", "--user", "daemon-reload").Run()
	_ = exec.Command("systemctl", "--user", "enable", appName+".service").Run()
	return nil
}

func uninstallSystemd(appName string) error {
	target, err := systemdUnitPath(appName)
	if err != nil {
		return err
	}
	if !pathExists(target) {
		return ErrNotInstalled
	}
	_ = exec.Command("systemctl", "--user", "stop", appName+".service").Run()
	_ = exec.Command("systemctl", "--user", "disable", appName+".service").Run()
	if err := os.Remove(target); err != nil {
		return err
	}
	_ = exec.Command("systemctl", "--user", "daemon-reload").Run()
	return nil
}

func systemdStatus(appName string) (ServiceState, error) {
	target, err := systemdUnitPath(appName)
	if err != nil {
		return ServiceState{}, err
	}
	st := ServiceState{
		Path:    target,
		Backend: "systemd-user",
		Note:    "enable `loginctl enable-linger` to keep the service running after logout",
	}
	st.Installed = pathExists(target)
	if st.Installed {
		out, _ := exec.Command("systemctl", "--user", "is-active", appName+".service").Output()
		st.Active = strings.TrimSpace(string(out)) == "active"
	}
	return st, nil
}
