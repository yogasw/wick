// Package session manages on-disk session entries at
// `<BaseDir>/sessions/<id>/`. A session is one Slack thread or one UI
// conversation: routing key, status, agent registry, log files, and an
// optional reference to a project whose folder is the agent cwd.
//
// Sessions own no project files of their own — a project is a
// shared bundle (see internal/agents/project) whose folder the pool
// resolves to a cwd at spawn time. See agents-design.md §0.2 for the
// refactor that removed the per-session git worktree, and the project
// design (internal/planning/archive/project/design.md) for the Workspace→Project
// rename.
//
// Files in this package:
//   - session.go — Meta + lifecycle (Create/Load/Save/Delete/SetProject)
//   - agents.go  — per-session AgentEntry + Add/SetActive
package session

import (
	"context"
	"fmt"
	"os"
	"time"

	"github.com/yogasw/wick/internal/agents/config"
	"github.com/yogasw/wick/internal/agents/project"
	"github.com/yogasw/wick/internal/agents/storage"
)

// Origin identifies where the session was first created from. Slack
// threads use thread_ts as the session ID; UI/API sessions use a UUID
// generated by wick.
type Origin string

const (
	OriginSlack    Origin = "slack"
	OriginTelegram Origin = "telegram"
	OriginREST     Origin = "rest"
	OriginUI       Origin = "ui"
)

// Status is the high-level pool view: idle (no subprocess), queued
// (waiting for a slot), running (subprocess alive).
type Status string

const (
	StatusIdle    Status = "idle"
	StatusQueued  Status = "queued"
	StatusRunning Status = "running"
)

// Meta is the persisted-on-disk shape of a session. PendingInput is
// the message buffer that survives wick restart so a session that was
// queued at shutdown gets its messages drained on next boot.
type Meta struct {
	// ProjectID references the project this session belongs to. Empty =
	// unscoped (pool falls back to the tools-config default project).
	ProjectID    string    `json:"project_id,omitempty"`
	Origin       Origin    `json:"origin"`
	ChannelID    string    `json:"channel_id,omitempty"`
	ActiveAgent  string    `json:"active_agent,omitempty"`
	Status       Status    `json:"status"`
	CreatedAt    time.Time `json:"created_at"`
	LastActive   time.Time `json:"last_active"`
	PendingInput []string  `json:"pending_input,omitempty"`
	// Label is the session title shown in the sidebar. By default it is
	// the first user message truncated to 60 runes (see
	// pool.setLabelIfEmpty), cached here so sidebar rendering never needs
	// to open conversation.jsonl. Once a human or the agent sets an
	// explicit title (TitleCustom=true) the auto-derived label no longer
	// overwrites it.
	Label string `json:"label,omitempty"`
	// TitleCustom marks Label as an explicit title set by a human or the
	// agent (via the wick_set_title MCP tool), as opposed to the
	// auto-derived first-message label. When true, the first-user-message
	// auto-label is skipped so it never clobbers the chosen title.
	TitleCustom bool `json:"title_custom,omitempty"`
	// Preset is the name of the preset active for this session.
	// Factory reads the preset content from presets/<name>/agent.md on
	// every spawn so edits to the preset take effect on next respawn.
	Preset string `json:"preset,omitempty"`
	// Subscribers is the list of user IDs that opted in to receive
	// browser push notifications for this session's lifecycle
	// transitions (queue → working → idle). Persisted in meta.json so
	// the subscription survives wick restart. Sessions are shared
	// across users (anyone can open them) but pushes target only the
	// IDs in this list — no auto-subscribe on session create.
	Subscribers []string `json:"subscribers,omitempty"`
	// UserID is the wick user ID that created this session. Empty for
	// legacy sessions created before ownership tracking was added.
	// When non-empty, only the owning user (or app owner) may access it.
	UserID string `json:"user_id,omitempty"`
}

// IsSubscribed returns true when userID has opted in to receive
// lifecycle push notifications for this session.
func (m *Meta) IsSubscribed(userID string) bool {
	if m == nil || userID == "" {
		return false
	}
	for _, id := range m.Subscribers {
		if id == userID {
			return true
		}
	}
	return false
}

// AddSubscriber appends userID to Subscribers if not already present.
// Returns true if the list changed (i.e. caller should persist Meta).
func (m *Meta) AddSubscriber(userID string) bool {
	if m == nil || userID == "" {
		return false
	}
	for _, id := range m.Subscribers {
		if id == userID {
			return false
		}
	}
	m.Subscribers = append(m.Subscribers, userID)
	return true
}

// RemoveSubscriber drops userID from Subscribers. Returns true if the
// list changed.
func (m *Meta) RemoveSubscriber(userID string) bool {
	if m == nil || userID == "" {
		return false
	}
	for i, id := range m.Subscribers {
		if id == userID {
			m.Subscribers = append(m.Subscribers[:i], m.Subscribers[i+1:]...)
			return true
		}
	}
	return false
}

// Session is the in-memory view: ID + meta + agent registry. Mirrors
// the trio of files at sessions/<id>/.
type Session struct {
	ID     string       `json:"id"`
	Meta   Meta         `json:"meta"`
	Agents []AgentEntry `json:"agents"`
}

// CreateOptions describes a new session. ProjectID is optional — a
// session may be created without one, in which case the pool falls
// back to the tools-config default project (or a per-session temp
// dir if no default is set).
type CreateOptions struct {
	ID        string
	ProjectID string
	Origin    Origin
	ChannelID string
	// Preset is the preset name to associate with this session.
	// Stored in meta.json; factory loads content from presets/<name>/agent.md on spawn.
	Preset string
	// UserID is the wick user who is creating this session. Stored in
	// meta.json for ownership checks in MCP handlers.
	UserID string
}

// Create materializes sessions/<id>/: meta.json, agents.json (empty
// array), agent.md snapshot. No filesystem work touches the project
// folder — project folders live under projects/<id>/ and are shared
// across sessions; the session simply stores the project id as a
// reference.
func Create(_ context.Context, layout config.Layout, opt CreateOptions) (Session, error) {
	if err := storage.ValidateSessionID(opt.ID); err != nil {
		return Session{}, err
	}
	if opt.Origin == "" {
		opt.Origin = OriginUI
	}
	if opt.ProjectID != "" && !project.Exists(layout, opt.ProjectID) {
		return Session{}, fmt.Errorf("project %q not found", opt.ProjectID)
	}
	dir := layout.SessionDir(opt.ID)
	if storage.PathExists(dir) {
		return Session{}, fmt.Errorf("session %q already exists", opt.ID)
	}
	if err := os.MkdirAll(dir, 0o755); err != nil {
		return Session{}, err
	}

	now := time.Now().UTC()
	meta := Meta{
		ProjectID:  opt.ProjectID,
		Origin:     opt.Origin,
		ChannelID:  opt.ChannelID,
		Preset:     opt.Preset,
		Status:     StatusIdle,
		CreatedAt:  now,
		LastActive: now,
		UserID:     opt.UserID,
	}
	if err := storage.WriteJSON(layout.SessionMeta(opt.ID), &meta); err != nil {
		_ = os.RemoveAll(dir)
		return Session{}, err
	}
	if err := storage.WriteJSON(layout.SessionAgents(opt.ID), []AgentEntry{}); err != nil {
		_ = os.RemoveAll(dir)
		return Session{}, err
	}
	return Session{ID: opt.ID, Meta: meta, Agents: []AgentEntry{}}, nil
}

// Load reads meta.json + agents.json. Missing agents.json is treated
// as an empty list.
func Load(layout config.Layout, id string) (Session, error) {
	if err := storage.ValidateSessionID(id); err != nil {
		return Session{}, err
	}
	var meta Meta
	if err := storage.ReadJSON(layout.SessionMeta(id), &meta); err != nil {
		return Session{}, err
	}
	var agents []AgentEntry
	if storage.PathExists(layout.SessionAgents(id)) {
		if err := storage.ReadJSON(layout.SessionAgents(id), &agents); err != nil {
			return Session{}, err
		}
	}
	return Session{ID: id, Meta: meta, Agents: agents}, nil
}

// SaveMeta atomically rewrites sessions/<id>/meta.json.
func SaveMeta(layout config.Layout, id string, meta Meta) error {
	if err := storage.ValidateSessionID(id); err != nil {
		return err
	}
	if !storage.PathExists(layout.SessionDir(id)) {
		return fmt.Errorf("session %q not found", id)
	}
	return storage.WriteJSON(layout.SessionMeta(id), &meta)
}

// List returns every session ID, sorted.
func List(layout config.Layout) ([]string, error) {
	return storage.ScanDirNames(layout.SessionsDir())
}

// Delete removes the session folder. The project it points at is
// left untouched — projects are shared, so deleting a session must
// not delete its files.
func Delete(_ context.Context, layout config.Layout, id string) error {
	if err := storage.ValidateSessionID(id); err != nil {
		return err
	}
	return os.RemoveAll(layout.SessionDir(id))
}

// SetProject changes the project a session points at. No filesystem
// work — the next agent spawn will resolve the new project's folder
// as cwd. Conversation, agent registry, and logs are preserved.
// Empty newProjectID unscopes the session.
func SetProject(_ context.Context, layout config.Layout, id, newProjectID string) error {
	if newProjectID != "" && !project.Exists(layout, newProjectID) {
		return fmt.Errorf("project %q not found", newProjectID)
	}
	sess, err := Load(layout, id)
	if err != nil {
		return err
	}
	if sess.Meta.ProjectID == newProjectID {
		return nil
	}
	sess.Meta.ProjectID = newProjectID
	sess.Meta.LastActive = time.Now().UTC()
	return SaveMeta(layout, id, sess.Meta)
}
